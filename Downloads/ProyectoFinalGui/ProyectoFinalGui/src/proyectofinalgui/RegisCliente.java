    private void RegistarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistarClienteActionPerformed
        // obterner los datos del cliente y guardarlos en un arrayList
        
                                                   

    if (this.NombreTxt.getText().isEmpty() || this.EdadTxt.getText().isEmpty() || this.TelefonoTxt.getText().isEmpty() || this.DireccionTxt.getText().isEmpty() || (!this.rbMasculino.isSelected() && !this.rbFemenino.isSelected())) {
        JOptionPane.showMessageDialog(null, "DEBE LLENAR TODOS LOS ESPACIOS", null, JOptionPane.ERROR_MESSAGE);
    } else {
        String genero = "";

        if (this.rbMasculino.isSelected()) {
            genero = "Masculino";
        } else if (this.rbFemenino.isSelected()) {
            genero = "Femenino";
        }

        DefaultTableModel modelo = (DefaultTableModel) this.jTable2.getModel();

        int newCode = 1;
        if (modelo.getRowCount() > 0) {
            int lastCode = Integer.parseInt(modelo.getValueAt(modelo.getRowCount() - 1, 0).toString());
            newCode = lastCode + 1;
        }

        modelo.addRow(new Object[]{String.valueOf(newCode), this.NombreTxt.getText(), this.EdadTxt.getText(), genero, this.TelefonoTxt.getText(), this.DireccionTxt.getText()});

        lista.clear();

        for (int i = 0; i < this.jTable2.getRowCount(); i++) {
            lista.add(new Cliente(Integer.parseInt(String.valueOf(this.jTable2.getValueAt(i, 0))), String.valueOf(this.jTable2.getValueAt(i, 1)), Integer.parseInt(String.valueOf(this.jTable2.getValueAt(i, 2))), String.valueOf(this.jTable2.getValueAt(i, 3)), String.valueOf(this.jTable2.getValueAt(i, 4)), String.valueOf(this.jTable2.getValueAt(i, 5))));
        }

        JOptionPane.showMessageDialog(null, "Su Registro Fue Exitoso");

        NombreTxt.setText("");
        EdadTxt.setText("");
        this.rbMasculino.setSelected(false);
        this.rbFemenino.setSelected(false);
        TelefonoTxt.setText("");
        DireccionTxt.setText("");
    }




    }//GEN-LAST:event_RegistarClienteActionPerformed

