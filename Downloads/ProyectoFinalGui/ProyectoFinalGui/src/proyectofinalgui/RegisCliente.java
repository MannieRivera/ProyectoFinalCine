    private void EdadTxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_EdadTxtKeyTyped
        int tecla = evt.getKeyChar();

        boolean num = tecla >= 48 && tecla <= 57;

        if (!(num)) {
            evt.consume();
        }

        if (this.EdadTxt.getText().trim().length() == 2)
            evt.consume();
    }//GEN-LAST:event_EdadTxtKeyTyped

